package org.eql.autom.orangehrm.pageobject;

public class InvalidTableRowNumberException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8146035234460271359L;

	public InvalidTableRowNumberException() {
		super();
	}

	public InvalidTableRowNumberException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public InvalidTableRowNumberException(String message, Throwable cause) {
		super(message, cause);
	}

	public InvalidTableRowNumberException(String message) {
		super(message);
	}

	public InvalidTableRowNumberException(Throwable cause) {
		super(cause);
	}


}
