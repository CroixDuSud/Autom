package fr.eql.autom.dictionnaire.exceptions;

public class ProprieteDupliqueeException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2899457788132911755L;

	public ProprieteDupliqueeException() {
		super();
	}

	public ProprieteDupliqueeException(String message, Throwable cause) {
		super(message, cause);
	}

	public ProprieteDupliqueeException(String message) {
		super(message);
	}

	public ProprieteDupliqueeException(Throwable cause) {
		super(cause);
	}

	
}
